// ═══════════════════════════════════════════════════════════════════════════
//  Test 3: Validate Aeneas output shape
// ═══════════════════════════════════════════════════════════════════════════

/// Verify that when Aeneas runs, it produces files with expected structure.
#[tokio::test]
#[ignore = "requires Docker + Aeneas binary"]
async fn aeneas_output_has_expected_structure() {
    let _ = tracing_subscriber::fmt().try_init();

    println!("═══ Aeneas Output Structure Validation ═══\n");

    if !aeneas_available().await {
        eprintln!("SKIP: Aeneas binary not available");
        return;
    }

    // Check that aeneas prints version info and has expected flags
    let version = tokio::process::Command::new("aeneas")
        .arg("--version")
        .output()
        .await
        .expect("run aeneas --version");

    let version_str = String::from_utf8_lossy(&version.stdout);
    println!("Aeneas version:\n{}", version_str);

    let help = tokio::process::Command::new("aeneas")
        .arg("--help")
        .output()
        .await
        .expect("run aeneas --help");

    let help_str = String::from_utf8_lossy(&help.stdout);

    // Look for expected flags
    assert!(
        help_str.contains("--dest") || help_str.contains("dest"),
        "aeneas --help should mention output directory flag"
    );

    assert!(
        help_str.contains("--backend") || help_str.contains("backend"),
        "aeneas --help should mention backend selection"
    );

    assert!(
        help_str.contains("lean"),
        "aeneas should list 'lean' as an available backend"
    );

    println!("✓ Aeneas has expected interface (--dest, --backend lean)");
}
