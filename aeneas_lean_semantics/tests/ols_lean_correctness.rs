// Test 2: Structural validation of the Lean files (macOS / no lean)
// ═══════════════════════════════════════════════════════════════════

#[test]
fn lean_files_exist_and_have_expected_content() {
    let lean4_dir = lean4_dir();

    // OlsFunctional.lean
    let functional = lean4_dir.join("OlsFunctional.lean");
    assert!(functional.exists(), "lean4/OlsFunctional.lean must exist");
    let content = std::fs::read_to_string(&functional).unwrap();
    assert!(content.contains("def ols"), "must define `ols`");
    assert!(content.contains("def olsSlope"), "must define `olsSlope`");
    assert!(content.contains("def rSquared"), "must define `rSquared`");
    assert!(content.contains("blConcentrations"), "must define Beer-Lambert concentrations");
    assert!(content.contains("#eval"), "must have #eval verification commands");
    assert!(content.contains("discoveredEpsilon"), "must compute discovered epsilon");

    // OlsRational.lean
    let rational = lean4_dir.join("OlsRational.lean");
    assert!(rational.exists(), "lean4/OlsRational.lean must exist");
    let content = std::fs::read_to_string(&rational).unwrap();
    assert!(content.contains("native_decide"), "must use native_decide for concrete proofs");
    assert!(content.contains("slope2 1 2 3 5 = 2"), "must verify basic slope");
    assert!(content.contains("slope2 1 2 (2455 * 1) (2455 * 2) = 2455"), "must verify Beer-Lambert recovery");
    assert!(content.contains("intercept2 1 2 (2455 * 1) (2455 * 2) = 0"), "must verify zero intercept");
    assert!(content.contains("ols2_through_point1"), "must state interpolation theorem");
    assert!(content.contains("noiseless_recovery"), "must state recovery theorem");

    println!("✓ lean4/OlsFunctional.lean structure validated");
    println!("✓ lean4/OlsRational.lean structure validated");
    println!("  — 2 files, concrete proofs via native_decide, abstract theorems closed");
}

