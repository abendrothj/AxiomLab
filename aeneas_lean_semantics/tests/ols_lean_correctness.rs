// fn linear_regression(_1: &[f64], _2: &[f64]) -> Option<LinearFit>
//
// bb0: {
//     _3 = Len(*_1);                          // n = x.len()
//     _4 = Len(*_2);                          // m = y.len()
//     switchInt(move _3 < 2) -> [0: bb1, otherwise: bb_none];
// }
// bb1: {
//     _5 = Ne(move _3, move _4) -> [true: bb_none, false: bb2];
// }
// bb2: {
//     // Build design matrix A: DMatrix<f64>(n × 2) where col0 = x, col1 = 1
//     _6 = DMatrix::from_fn(move _3, const 2_usize, |r, c| ...);
//     // Normal equations: (AᵀA)β = Aᵀb
//     _7 = Transpose(ref _6);
//     _8 = Mul(move _7, ref _6);              // AᵀA
//     _9 = Mul(move _7, ref _b);              // Aᵀb
//     _10 = LU::new(move _8);                 // LU factorization
//     _11 = LU::solve(ref _10, ref _9);       // β = (AᵀA)⁻¹Aᵀb
