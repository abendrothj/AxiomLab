fn lean4_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .expect("workspace root")
        .join("lean4")
}

fn lean_binary() -> PathBuf {
    if let Ok(p) = std::env::var("LEAN_PATH_BIN") {
        let path = PathBuf::from(&p).join("lean");
        if path.exists() {
            return path;
        }
    }
    // Try elan-managed lean
    let elan_lean = PathBuf::from("/opt/elan/bin/lean");
    if elan_lean.exists() {
        return elan_lean;
    }
    PathBuf::from("lean")
}

fn lean_available() -> bool {
    let lean = lean_binary();
    if lean.exists() {
        return true;
    }
    std::process::Command::new(&lean)
        .arg("--version")
        .output()
        .is_ok()
}

fn lean_uses_lake_env() -> bool {
    lean4_dir().join("lakefile.lean").exists() || lean4_dir().join("lakefile.toml").exists()
}

