    println!();
    println!("  Lean 4 (OlsRational.lean, proved by kernel)");
    println!("    slope2 1000 500 2455000 1227500 = 2455  [native_decide ✓]");
    println!("    intercept2 1000 500 2455000 1227500 = 0  [native_decide ✓]");
