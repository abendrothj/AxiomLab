const AENEAS_LEAN_EXCERPT: &str = r#"
-- Auto-generated by Aeneas from scientific_compute::discovery::linear_regression
-- See lean4/OlsFunctional.lean for full file

namespace AxiomLab.Ols

structure LinearCoeffs where
  slope     : Float
  intercept : Float

def predict (β : LinearCoeffs) (x : Float) := β.slope * x + β.intercept

def olsSlope (xs ys : List Float) : Float :=
  let x_bar := mean xs; let y_bar := mean ys
  let num   := (List.zipWith (fun x y => (x - x_bar) * (y - y_bar)) xs ys).foldl (·+·) 0
  let den   := (xs.map (fun x => let d := x - x_bar; d * d)).foldl (·+·) 0
  if den == 0 then 0 else num / den

def ols (xs ys : List Float) : LinearCoeffs :=
  { slope := olsSlope xs ys, intercept := mean ys - olsSlope xs ys * mean xs }

end AxiomLab.Ols
"#;

