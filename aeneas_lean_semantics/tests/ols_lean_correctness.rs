// Test 4: The aeneas/lean crate infrastructure is wired up
// ═══════════════════════════════════════════════════════════════════

#[test]
fn aeneas_lean_modules_available() {
    use aeneas_lean_semantics::lean::find_lean;
    use aeneas_lean_semantics::aeneas::find_aeneas;

    // `find_lean` / `find_aeneas` return the resolved path (or fallback).
    // Here we go further and test whether the binary is actually executable.
    let lean_executable = std::process::Command::new("lean")
        .arg("--version")
        .output()
        .is_ok();
    let aeneas_executable = std::process::Command::new("aeneas")
        .arg("--help")
        .output()
        .is_ok();

    if lean_executable {
        println!("✓ lean binary is executable");
    } else {
        println!("  lean binary not found on PATH (expected outside Docker)");
    }
    if aeneas_executable {
        println!("✓ aeneas binary is executable");
    } else {
        println!("  aeneas binary not found on PATH (expected outside Docker)");
    }

    // The module functions themselves must not panic.
    assert!(find_lean().is_ok(), "find_lean should not error");
    assert!(find_aeneas().is_ok(), "find_aeneas should not error");

    // If running inside Docker, both should be executable.
    if std::env::var("AXIOMLAB_DOCKER").is_ok() {
        assert!(
            lean_executable,
            "lean binary must be executable inside Docker container"
        );
        assert!(
            aeneas_executable,
            "aeneas binary must be executable inside Docker container"
        );
    }
}
